
public class Variaveis {
	
	public static void main(String[] args) {
		
		int idade = 20;
		
		System.out.println("A idade é: " + idade + " anos.");
		
		double salario = 3890.87;
		
		System.out.println("O Salário é: R$ " + salario);
		
		double divisaoDoublePorInt;
		double divisaoIntPorDouble;
		int numero1 = 10;
		double numero2 = 3.14;
		
		//A variavel que receberá a divisao deve ser do tipo double e pelo menos uma das variaves do calculo deverar se do tipo double.
		
		divisaoDoublePorInt = (numero2 / numero1);
		System.out.println("Resultado da divisao: " + divisaoDoublePorInt);
		
		divisaoIntPorDouble = (numero1 / numero2);
		System.out.println("Resultado da divisao: " + divisaoIntPorDouble);
		
		
		
		
		
		
		
		
	}

}
